package main

import "fmt"

func main() {

	fmt.Printf("%d - %b - %X \n", 50, 50, 50)
	fmt.Printf("%d - %b - %x \n", 50, 50, 50)
	fmt.Printf("%d - %b - %X \t", 50, 50, 50)
	fmt.Printf("%d - %b - %x \t", 50, 50, 50)
	fmt.Printf("%d - %b - %#X \t", 50, 50, 50)
	fmt.Printf("%d - %b - %#x \t", 50, 50, 50)
}
